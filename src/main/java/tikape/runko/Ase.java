package tikape.runko;

public class Ase {
    private String nimi;
    private String asenumero;
    
    public Ase(String nimi, String asenumero) {
        this.nimi = nimi;
        this.asenumero = asenumero;
    }
    
    public String getNimi() {
        return nimi;
    }
    
    public String getNumero() {
        return asenumero;
    }
}
